    private void trActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_trActionPerformed
        DefaultMutableTreeNode root = new DefaultMutableTreeNode("Datos de la Tabla");
        //filas
        for(int i = 0; i < tabla.getRowCount(); i++){
            
            int rowIndex = i + 1; //
            
            DefaultMutableTreeNode row = new DefaultMutableTreeNode("Row" + rowIndex);
            //columnas
            for(int c = 0; c < tabla.getColumnCount(); c++){
                
                DefaultMutableTreeNode node = new DefaultMutableTreeNode(tabla.getValueAt(i, c));
                row.add(node);
                
            }
            root.add(row);
        }
        DefaultTreeModel model = new DefaultTreeModel(root);
        Arbol.setModel(model); 
        
    }//GEN-LAST:event_trActionPerformed

